  add column if not exists category text not null default 'News',
  add column if not exists summary text not null default '',
  add column if not exists content text not null default '',
  add column if not exists image text not null default '',
  add column if not exists link text not null default '',
  add column if not exists published boolean not null default true,
  add column if not exists published_at timestamptz not null default now();

-- ---------------------------------------------------------------------------
-- Functions
-- ---------------------------------------------------------------------------

create or replace function public.increment_cause_raised(
  cause_row_id bigint,
  increment_amount numeric
)
returns void
language sql
security definer
set search_path = public
as $$
  update public.causes
  set raised = raised + greatest(coalesce(increment_amount, 0), 0)
  where id = cause_row_id;
$$;

revoke all on function public.increment_cause_raised(bigint, numeric) from public;
grant execute on function public.increment_cause_raised(bigint, numeric) to service_role;

