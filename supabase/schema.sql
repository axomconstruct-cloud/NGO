-- ---------------------------------------------------------------------------
-- Row Level Security
--
-- The Vercel API uses SUPABASE_SERVICE_ROLE_KEY, which bypasses RLS.
-- Browser clients receive read-only public access to published content and
-- insert-only access to public forms. No public update/delete policies exist.
-- ---------------------------------------------------------------------------

alter table public.admin_users enable row level security;
alter table public.settings enable row level security;
alter table public.causes enable row level security;
alter table public.stories enable row level security;
alter table public.events enable row level security;
alter table public.news enable row level security;
alter table public.gallery enable row level security;
alter table public.team_members enable row level security;
alter table public.partners enable row level security;
alter table public.donations enable row level security;
alter table public.volunteers enable row level security;
alter table public.contacts enable row level security;
alter table public.subscribers enable row level security;

