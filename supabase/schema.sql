on public.volunteers for insert
to anon, authenticated
with check (
  length(trim(name)) > 0
  and length(trim(email)) > 3
  and length(trim(phone)) > 0
  and length(trim(interest)) > 0
);

drop policy if exists "public_create_contact" on public.contacts;
create policy "public_create_contact"
on public.contacts for insert
to anon, authenticated
with check (
  length(trim(name)) > 0
  and length(trim(email)) > 3
  and length(trim(subject)) > 0
  and length(trim(message)) > 0
);

drop policy if exists "public_create_subscriber" on public.subscribers;
create policy "public_create_subscriber"
on public.subscribers for insert
to anon, authenticated
with check (length(trim(email)) > 3);

